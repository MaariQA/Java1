//  toUpperCase / toUpperCase
//  convert all char in this string to upper case 
package STRING02;
public class ToUpperCaseToLowerCase {
	public static void main(String[] args) {
	//  to convert in uppercase: toUpperCase
		String str = "Mayur";
		String string = str.toUpperCase();
		System.out.println(string);
		
	//to convert in uppercase: toLowerCase
        String str2 = "WELCOME TO GOA SINGHAM";
        str2 = str2.toLowerCase();
        System.out.println(str2);
	}}
