// Return true if the string is empty or contain only with space else false
package STRING02;
public class IsBlank {
	public static void main(String[] args) {
		String str1 = "";                           // it return boolean value
		System.out.println("isBlank:" +str1.isBlank());
		
		str1 = " ";
		System.out.println("isBlank:" +str1.isBlank());
		
	}

}
