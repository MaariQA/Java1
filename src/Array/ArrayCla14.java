// for each
package Array;
public class ArrayCla14 {
	public static void main(String[] args) {
		String[] StrArr ={"Virat","Vijay","Kunal","Maari"};
		
		// Using traditional for each loop printing in normal order
		
		System.out.println("In normal 1 order");
		for(int i=0; i<StrArr.length; i++)
			System.out.println(StrArr[i]);
	}
}
